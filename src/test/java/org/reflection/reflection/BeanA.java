public class BeanA {

	private int inta;
	private Integer intb;
	private String stra;
	private boolean ba;
	private Boolean bb;
	private char ca;

	public int getInta() {
		return inta;
	}

	public void setInta(int inta) {
		this.inta = inta;
	}

	public Integer getIntb() {
		return intb;
	}

	public void setIntb(Integer intb) {
		this.intb = intb;
	}

	public String getStra() {
		return stra;
	}

	public void setStra(String stra) {
		this.stra = stra;
	}

	public boolean isBa() {
		return ba;
	}

	public void setBa(boolean ba) {
		this.ba = ba;
	}

	public Boolean getBb() {
		return bb;
	}

	public void setBb(Boolean bb) {
		this.bb = bb;
	}

	public char getCa() {
		return ca;
	}

	public void setCa(char ca) {
		this.ca = ca;
	}
}
